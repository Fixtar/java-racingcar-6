package racingcar;

public class RacingCar {

    private String name;

    private int count;

    public RacingCar(String name, int count) {
        this.name = name;
        this.count = count;
    }

    public String getName() {
        return this.name;
    }

    public int getCount() {
        return this.count;
    }

}
